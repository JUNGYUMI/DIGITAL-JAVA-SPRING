package kr.green.springcafe.vo;

public class OrderVo {
	private int or_num;
	private String or_mem_id;
	private String or_time;
	private int or_price;
	
	public int getOr_num() {
		return or_num;
	}
	public void setOr_num(int or_num) {
		this.or_num = or_num;
	}
	public String getOr_mem_id() {
		return or_mem_id;
	}
	public void setOr_mem_id(String or_mem_id) {
		this.or_mem_id = or_mem_id;
	}
	public String getOr_time() {
		return or_time;
	}
	public void setOr_time(String or_time) {
		this.or_time = or_time;
	}
	public int getOr_price() {
		return or_price;
	}
	public void setOr_price(int or_price) {
		this.or_price = or_price;
	}
	
	
}
